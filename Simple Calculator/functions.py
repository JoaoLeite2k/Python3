class Math():
    def numSum(a, b):
        return a + b

    def numSubt(a, b):
        return a - b

    def numMult(a, b):
        return int(a) * int(b)

    def numDiv(a, b):
        return a / b

    def numExpo(a, b):
        return a ** b

    def numSRoot(a):
        return a ** (1/2)
























